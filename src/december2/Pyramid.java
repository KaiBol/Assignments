package december2;


		
    public class Pyramid {

	public static void main(String[] args) {
				System.out.println("Pyramid pattern of star in Java : ");
				
	
				for (int i = 0; i < 4; i++) { 
					for (int j = i; j < 4; j++) 
						System.out.print(" ");
				for (int k = 0; k <= (i*2); k++) 	
					System.out.print("*");
						 
					
				System.out.println(); 
	} 

	} 
	
	}	
	
	


